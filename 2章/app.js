// 2章【JavaScriptの基本文法】

// 変数
// var 変数名 = 内容;
var hello = "Hello World!";
alert(hello);

// 整数、負数を代入
var int1 = 1;
var int2 = -19;
alert(int1);
alert(int2);

// 小数点を代入
var float1 = 3.14;
alert(float1);

// 文字列を代入
var str1 = "本日も晴天なり";
alert(str1);

// 四則演算
// 足し算
alert(3 + 2);

// 引き算
alert(3 - 9);

// 掛け算
alert(2 * 9);

// 割り算
alert(10 / 4);

// 除算
alert(6 % 2);

// 文字列の結合
var str1 = "文字列の";
var str2 = "結合";
alert(str1 + str2);